CREATE OR REPLACE FUNCTION BD_TRAMITE_CERO_PAPEL.FN_OBT_ID_UBIGEO (P_TXT_REGION VARCHAR2,P_TXT_PROVINCIA VARCHAR2, P_TXT_DISTRITO VARCHAR2)
RETURN NUMBER IS
RESULTADO CP_TBL_CIUDADANO.ID_UBIGEO_DOMICILIO%TYPE;
V_REGION NUMBER;
V_PROVINCIA NUMBER;

V_TXTPROVINCIA VARCHAR2(200):=P_TXT_PROVINCIA;
V_TXTDISTRITO VARCHAR2(300):=P_TXT_DISTRITO;


BEGIN
	
  SELECT ID_UBIGEO INTO V_REGION FROM BD_ECODOC_PLUS.UBIGEO WHERE PADRE  IS NULL AND UPPER(NOMBRE) = UPPER(P_TXT_REGION); 
  IF V_REGION = 2810 THEN
    V_TXTPROVINCIA:=P_TXT_REGION;
    V_TXTDISTRITO:=P_TXT_PROVINCIA;
   
  END IF;
  SELECT ID_UBIGEO INTO V_PROVINCIA FROM BD_ECODOC_PLUS.UBIGEO WHERE PADRE = V_REGION AND UPPER(NOMBRE) = UPPER(V_TXTPROVINCIA);
  SELECT ID_UBIGEO INTO RESULTADO FROM BD_ECODOC_PLUS.UBIGEO WHERE PADRE = V_PROVINCIA AND UPPER(NOMBRE) = UPPER(V_TXTDISTRITO); 

  RETURN RESULTADO;
END FN_OBT_ID_UBIGEO;



