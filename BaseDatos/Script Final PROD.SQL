CREATE OR REPLACE PROCEDURE BD_MINAM.USP_SEL_SG_ROL_IDUSU (
							 P_ID_USUARIO IN BD_MINAM.SEG_USUARIO.ID_USUARIO%TYPE DEFAULT NULL,
							 P_ID_SISTEMA IN BD_MINAM.SEG_ROL.ID_SISTEMA%TYPE DEFAULT NULL,	
							 p_ResultSet  OUT  SYS_REFCURSOR) 
							IS							
							BEGIN
							  OPEN p_ResultSet FOR
							  	 SELECT
								 ROL.ID_ROL,
							     ROL.NOMBRE,
							     ROL.DESCRIPCION
							     FROM BD_MINAM.SEG_ROL ROL INNER JOIN
							     BD_MINAM.SEG_USUARIO_ROL ur on (ROL.ID_ROL = ur.ID_ROL)
							     WHERE UR.ID_USUARIO = P_ID_USUARIO AND
							     ROL.ID_SISTEMA  = P_ID_SISTEMA;	  
							    
END USP_SEL_SG_ROL_IDUSU;


